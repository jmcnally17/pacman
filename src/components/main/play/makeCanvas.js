const makeCanvas = () => {
  const board = document.querySelector("#board");
  const ctx = board.getContext("2d");

  map.forEach((row, i) => {
    row.forEach((element, j) => {
      if (element === "-") {
        const boundary = new Boundary({
          position: {
            x: 40 * j,
            y: 40 * i,
          },
        });
        boundary.draw(ctx);
      }
    });
  });
};

module.exports = makeCanvas;
